#Módulo de operacões com strings

def capitalize_string(s):
    """Retorna a string com a primeira letra maiúscula"""
    return s.capitalize()

def reverse_string(s):
    """Retorna a string invertida"""
    return s[::-1] 

def count(s): 
    """Retorna o número de itens na string"""       
    return len(s)





